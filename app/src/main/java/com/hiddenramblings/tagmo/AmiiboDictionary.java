    public static String getNameById(int brand, int seq, int variant) {
        int id = (brand << 12) & (seq << 8) & variant;
        if (charList.containsKey(id))
            return charList.get(id);
        else
            return null;
    }

    public static String getNameByAmiiboId(int id) {
        if (idList.containsKey(id))
            return idList.get(id);
        return null;
    }

    public static String getDisplayName(byte[] chardata) {
        AmiiboIdData data = parseid(chardata);
        String name = getNameByAmiiboId(data.AmiiboId);
        if (name != null)
            return name;
        name = getNameById(data.Brand, data.BrandedIdSequence, data.Variant);
        if (name != null) {
            String seriesName = "UNKN";
            if (SERIES_NAME_SHORT.length > data.Series)
                seriesName = SERIES_NAME_SHORT[data.Series];
            return String.format("%s (%s)", name, seriesName);
        }
        return Util.bytesToHex(chardata);
    }

    public static class AmiiboIdData {
        public int Brand;
        public int BrandedIdSequence;
        public int Variant;
        public int Form;
        public int AmiiboId;
        public int Series;

        public AmiiboIdData(int brand, int brandedIdSequence, int variant, int form, int amiiboId, int series) {
            this.Brand = brand;
            this.BrandedIdSequence = brandedIdSequence;
            this.Variant = variant;
            this.Form = form;
            this.AmiiboId = amiiboId;
            this.Series = series;
        }

        @Override
        public String toString() {
            String seriesName = "UNKN";
            if (SERIES_NAME_SHORT.length > this.Series)
                seriesName = SERIES_NAME_SHORT[this.Series];

            return String.format("Brand:%03X\tSeq:%X\tVar:%02X\tForm:%02X\tAmiSeq:%04X\tSeries:%02X\t%s",
                    this.Brand, this.BrandedIdSequence, this.Variant, this.Form, this.AmiiboId, this.Series,
                    seriesName);
        }
    }

    static AmiiboIdData parseid(byte[] data) {
        int brand = (data[0x00] & 0xFF) << 4;
        brand |= (data[0x01] & 0xFF) >> 4;

        int charseq = data[0x01] & 0x0F;

        int variant = data[0x02] & 0xFF;

        int form = data[0x03] & 0xFF;

        int amiseq = (data[0x04] & 0xFF) << 8;
        amiseq |= data[0x05] & 0xFF;

        int series = data[0x06] & 0xFF;

        return new AmiiboIdData(brand, charseq, variant, form, amiseq, series);
    }

