package com.hiddenramblings.tagmo.amiibo;

import java.util.Date;

public class AmiiboReleaseDates {
    public final Date northAmerica;
    public final Date japan;
    public final Date europe;
    public final Date australia;

    public AmiiboReleaseDates(Date northAmerica, Date japan, Date europe, Date australia) {
        this.northAmerica = northAmerica;
        this.japan = japan;
        this.europe = europe;
        this.australia = australia;
    }

    public AmiiboReleaseDates() {
        this(null, null, null, null);
    }
}
