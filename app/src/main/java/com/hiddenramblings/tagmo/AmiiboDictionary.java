    public static String getNameById(int brand, int seq, int variant) {
        int id = (brand << 16) + (seq << 8) + variant;
        if (charList.containsKey(id))
            return charList.get(id);
        else
            return null;
    }

    public static String getNameByAmiiboId(int id) {
        if (idList.containsKey(id))
            return idList.get(id);
        return null;
    }

    public static String getDisplayName(byte[] chardata) {
        AmiiboIdData data = parseid(chardata);
        String name = getNameByAmiiboId(data.AmiiboId);
        if (name != null)
            return name;
        name = getNameById(data.Brand, data.Variant, data.Type);
        if (name != null) {
            String seriesName = "UNK";
            if (seriesShortNames.containsKey(data.Series))
                seriesName = seriesShortNames.get(data.Series);
            return String.format("%s (%s)", name, seriesName);
        }
        return Util.bytesToHex(chardata);
    }

    public static class AmiiboIdData {
        public int Brand;
        public int Variant;
        public int Type;
        public int AmiiboId;
        public int Series;

        public AmiiboIdData(int brand, int variant, int type, int amiiboId, int series) {
            this.Brand = brand;
            this.Variant = variant;
            this.Type = type;
            this.AmiiboId = amiiboId;
            this.Series = series;
        }

        @Override
        public String toString() {
            String seriesName = "UNK";
            if (seriesShortNames.containsKey(this.Series))
                seriesName = seriesShortNames.get(this.Series);

            return String.format("Brand:%04X\tVar:%02X\tType:%02X\tAmiId:%04X\tSeries:%04X\t%s",
                    this.Brand, this.Variant, this.Type, this.AmiiboId, this.Series,
                    seriesName);
        }
    }

    static AmiiboIdData parseid(byte[] data) {
        int brand = (data[0x00] & 0xFF) << 8;
        brand |= (data[0x01] & 0xFF);

        int variant = data[0x02] & 0xFF;

        int type = data[0x03] & 0xFF;

        int model = (data[0x04] & 0xFF) << 8;
        model |= (data[0x05] & 0xFF);

        int series = (data[0x06] & 0xFF) << 8;
        series |= (data[0x07] & 0xFF);

        return new AmiiboIdData(brand, variant, type, model, series);
    }
